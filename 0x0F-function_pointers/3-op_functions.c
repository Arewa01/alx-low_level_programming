#include "3-calc.h"
/**
* op_add - a function that performs the sum of a and b
* @a: first number
* @b: second number
* Return: the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function that performs the subtraction of b from a
 * @a: first number
 * @b: second number
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function that performs the multiplication of a and b
 * @a: first number
 * @b: second number
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function that performs the division of a and b
 * @a: first number
 * @b: second number
 * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a function that performs the modular division of a and b
 * @a: first number
 * @b: second number
 * Return: the modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

